<template>
  <div class="com-container">
    <div class="com-chart" ref="seller_ref"></div>
  </div>
</template>

<script>
import { mapState } from "vuex";
import { getThemeValue } from "@/utils/theme_utils";
import { eventBus } from "../router";
export default {
  
  data() {
    return {
      myChart: null,
      allData: null,
      curPage: 1, // 当前显示的页数
      totalPage: 0, // 总页数
      timer: null, // 定时器的标识
    };
  },
  created() {
    eventBus.$on('allData', eventData => {
      // console.log(eventData);
      this.allData = eventData.sort((a, b) => {
          return a.value - b.value;
        });
      // do something with eventData
    });
  },
  mounted() {
    // 在组件创建完成之后，进行回调函数的注册
    this.$socket.registerCallBack("sellerData", this.getData);
    this.initChart();
    // this.getData();
    this.$socket.send({
      action: "getData",
      socketType: "sellerData",
      chartName: "seller",
      value: "",
    });
    window.addEventListener("resize", this.screenAdapter);
    // 在页面加载完成的时候，主动进行屏幕的适配
    this.screenAdapter();
    // console.log(this.theme);
  },
  destroyed() {
    clearInterval(this.timer);
    // 在组件销毁时，需将监听器注销
    window.removeEventListener("resize", this.screenAdapter);
    this.$socket.unRegisterCallBack("sellerData");
  },
  computed: {
    ...mapState(["theme"]),
  },
  watch: {
    theme() {
      this.myChart.dispose(); // 销毁当前的图表
      this.initChart(); // 重新以最新的主题名称初始化图表对象
      this.screenAdapter(); // 完成屏幕适配
      this.updateChart(); // 更新图表展示
    },
  },
  methods: {
    // 初始化echartsInstance对象
    initChart() {
      this.myChart = this.$echarts.init(this.$refs.seller_ref, this.theme);
      // 对图表初始化配置的控制
      const initOption = {
        title: {
          text: "▎ 商品评论数量统计",
          textStyle: {
            fontSize: 66,
          },
          left: 20,
          top: 20,
        },
        grid: {
          top: "22%",
          left: "3%",
          right: "8%",
          bottom: "3%",
          containLabel: true, // 距离包含坐标轴上的文字
        },
        backgroundColor: getThemeValue(this.theme).bgColor,
        tooltip: {
          trigger: "axis",
          axisPointer: {
            type: 'shadow'
          },
        },
        xAxis: {
          type: "value",
          
        },
        yAxis: {
          type: "category",
          axisLabel : {//坐标轴刻度标签的相关设置。
          interval:0,
          fontSize: 8,
          formatter : function(params){
             var newParamsName = "";// 最终拼接成的字符串
                      var paramsNameNumber = params.length;// 实际标签的个数
                      var provideNumber = 10;// 每行能显示的字的个数
                      var rowNumber = Math.ceil(paramsNameNumber / provideNumber);// 换行的话，需要显示几行，向上取整
                      /**
                       * 判断标签的个数是否大于规定的个数， 如果大于，则进行换行处理 如果不大于，即等于或小于，就返回原标签
                       */
                      // 条件等同于rowNumber>1
                      if (paramsNameNumber > provideNumber) {
                          /** 循环每一行,p表示行 */
                          for (var p = 0; p < rowNumber; p++) {
                              var tempStr = "";// 表示每一次截取的字符串
                              var start = p * provideNumber;// 开始截取的位置
                              var end = start + provideNumber;// 结束截取的位置
                              // 此处特殊处理最后一行的索引值
                              if (p === rowNumber - 1) {
                                  // 最后一次不换行
                                  tempStr = params.substring(start, paramsNameNumber);
                              } else {
                                  // 每一次拼接字符串并换行
                                  tempStr = params.substring(start, end) + "\n";
                              }
                              newParamsName += tempStr;// 最终拼成的字符串
                          }

                      } else {
                          // 将旧标签的值赋给新标签
                          newParamsName = params;
                      }
                      //将最终的字符串返回
                      return newParamsName
          }}
        },
        series: [
          {
            type: "bar",
            barWidth: 66,
            label: {
              show: true,
              position: "right",
              textStyle: {
                color: getThemeValue(this.theme).labelColor,
              },
            },
            itemStyle: {
              barBorderRadius: [0, 33, 33, 0],
              // 指明颜色渐变的方向
              // 指明不同百分比之下颜色的值
              color: {
                type: "linear",
                x: 0,
                y: 0,
                x2: 1,
                y2: 0,
                colorStops: [
                  {
                    offset: 0,
                    color: "#5052EE", // 0% 处的颜色
                  },
                  {
                    offset: 1,
                    color: "#AB6EE5", // 100% 处的颜色
                  },
                ],
                global: false, // 缺省为 false
              },
            },
          },
        ],
      };
      this.myChart.setOption(initOption);

      // 对图表对象进行鼠标事件的监听
      this.myChart.on("mouseover", () => {
        clearInterval(this.timer);
      });
      this.myChart.on("mouseout", () => {
        this.startInterval();
      });
    },
    // 获取服务端的数据
    getData() {
      this.totalPage = 3
      
        // this.allData.length % 5 === 0
        //   ? this.allData.length / 5
        //   : this.allData.length / 5 + 1;
      this.updateChart();
      // 启动定时器
      this.startInterval();
    },
    // 更新图表
    updateChart() {
      const start = (this.curPage - 1) * 5;
      const end = this.curPage * 5;
      const showData = this.allData?this.allData.slice(start, end):[1,2,3,4,5];
      // console.log(this.allData,showData);
      const sellerName = showData.map((item) => {
        return item.name;
      });

      const sellerValue = showData.map((item) => {
        return item.value;
      });

      const dataOption = {
        yAxis: {
          data: sellerName,
        },
        series: [
          {
            data: sellerValue,
          },
        ],
      };

      this.myChart.setOption(dataOption);
    },
    startInterval() {
    //   eventBus.$on('allData', eventData => {
    //   console.log(eventData);
    //   this.allData = eventData.sort((a, b) => {
    //       return a.value - b.value;
    //     });
    //   // do something with eventData
    // });
      if (this.timer) {
        clearInterval(this.timer);
      }
      this.timer = setInterval(() => {
        this.curPage++;
        if (this.curPage > this.totalPage) {
          this.curPage = 1;
        }
        this.updateChart();
      }, 3000);
    },
    // 当浏览器的大小发生变化时，会调用的方法，来完成屏幕的适配
    screenAdapter() {
      const titleFontSize = (this.$refs.seller_ref.offsetWidth / 100) * 3.6;
      const adapterOption = {
        title: {
          textStyle: {
            fontSize: titleFontSize,
          },
        },
        tooltip: {
          axisPointer: {
            lineStyle: {
              width: titleFontSize,
            },
          },
        },
        series: [
          {
            barWidth: titleFontSize,
            itemStyle: {
              borderRadius: [0, titleFontSize / 2, titleFontSize / 2, 0],
            },
          },
        ],
      };
      this.myChart.setOption(adapterOption);
      // 手动调用图表对象的resize才能生效
      this.myChart.resize();
    },
  },
};
</script>

